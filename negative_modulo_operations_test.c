#include <stdio.h>

int	main(void)
{
	printf("5 %% 3 = %d\n", 5 % 3);
	printf("-5 %% 3 = %d\n", -5 % 3);
	printf("5 %% -3 = %d\n", 5 % -3);
	printf("-5 %% -3 = %d\n", -5 % -3);
	printf("-1 %% 10 = %d\n", -1 % 10);
	printf("-2 %% 10 = %d\n", -2 % 10);
	printf("-3 %% 10 = %d\n", -3 % 10);
	printf("-13 %% 10 = %d\n", -13 % 10);
	return (0);
}
